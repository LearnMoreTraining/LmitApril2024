package basicJava;

public class LoopsExamples {

    public static void main(String[] args) {

        String [] d = {"lmti","learnmore"};

        for(int k=0;k < d.length;k++){

            System.out.println(d[k]);

        }

        // 0 ; 0 < 2 ; +1
        // 1< 2 -true +1 = 2
        // 2 < 2


        int [] a = {45,78,100,50};

        for(int h=0;h<a.length;h++){

            System.out.println(a[h]);
        }


    }
}
